#include "sort.h"

/**
 * swap_nodes - Swaping the two nodes in the listint_t doubly-linked list
 * @h: A pointer to the main of the doubly-linked list
 * @n1: A pointer to the first node to be swaped.
 * @n2: The second node to swaped.
 */
void swap_nodes(listint_t **h, listint_t **n1, listint_t *n2)
{
	(*n1)->next = n2->next;
	if (n2->next != NULL)
		n2->next->prev = *n1;
	n2->prev = (*n1)->prev;
	n2->next = *n1;
	if ((*n1)->prev != NULL)
		(*n1)->prev->next = n2;
	else
		*h = n2;
	(*n1)->prev = n2;
	*n1 = n2->prev;
}

/**
 * insertion_sort_list - Sorting the doubly linked list of an integers
 * Using the insertion of sort algorithm
 * @list: A pointer to the main doubly-linked list of integers
 * Description: Prints the list after each swap on the node
 */
void insertion_sort_list(listint_t **list)
{
	listint_t *iter, *insert, *tmp;

	if (list == NULL || *list == NULL || (*list)->next == NULL)
		return;

	for (iter = (*list)->next; iter != NULL; iter = tmp)
	{
		tmp = iter->next;
		insert = iter->prev;
		while (insert != NULL && iter->n < insert->n)
		{
			swap_nodes(list, &insert, iter);
			print_list((const listint_t *)*list);
		}
	}
}
