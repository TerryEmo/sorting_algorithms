#include "sort.h"

/**
 * swap_ints - Swaping the two integers in a array
 * @a: The first integer to swap in a array
 * @b: The second integer to swap in a array
 */
void swap_ints(int *a, int *b)
{
	int tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

/**
 * selection_sort - Sorting out all the array of the integers in ascending arragement 
 * Selection sort algorithm
 * @array: A array of integers to be sorted
 * @size: The size of the array
 *
 * Description: Prints the array after each swap.
 */
void selection_sort(int *array, size_t size)
{
	int *min;
	size_t i, j;

	if (array == NULL || size < 2)
		return;

	for (i = 0; i < size - 1; i++)
	{
		min = array + i;
		for (j = i + 1; j < size; j++)
			min = (array[j] < *min) ? (array + j) : min;

		if ((array + i) != min)
		{
			swap_ints(array + i, min);
			print_array(array, size);
		}
	}
}
